package practice12_05_19.practice12_04_19Library;

public class DoWhileIntro {
    public static void main(String[] args) {
        //count from 1 to 10

        int x= 1;

        do{
            System.out.print(x+" ,");
            x++;
        }while(x<=10);
        System.out.println();
        System.out.println("______________________________________________");

        x=10;
        while(x>=0){
            System.out.println(x);
            x--;
        }











    }
}
